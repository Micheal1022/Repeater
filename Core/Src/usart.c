uint16_t N_CRC16(uint8_t *data,uint8_t len)
{
	uint8_t uchCRCHi = 0xff;
	uint8_t uchCRCLo = 0xff;
	uint8_t uindex;
	while (len--) {
		uindex = uchCRCHi ^ *(data++);
		uchCRCHi = uchCRCLo ^ auchCRCHi[uindex];
		uchCRCLo = auchCRCLo[uindex];
	}
	return (uchCRCHi << 8 | uchCRCLo);
}

